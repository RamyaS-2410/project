public class elseif {
	public static void main(String[] args) {
	    int time = 13;
	    if (time < 10) {
	      System.out.println("Good morning.");
	    } 
	    else if (time < 18) {
	      System.out.println("Good day.");
	    }  
	    else if (time>5) {
	    	System.out.println("nice day");  
	    	}
	    else {
	      System.out.println("Good evening.");
	    }
	  }
	}

